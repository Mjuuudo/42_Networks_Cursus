#include "ft_printf.h"

int	ft_putstr(char *str)
{
	size_t	len;

	len = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[len] != '\0')
		len++;
	write(1, str, len);
	return (len);
}
